p_blue:						; loop b 0-3
  cpi  r19, 4
  breq p_done
  ldi  r22, 0				; g=0

p_green:					; loop g 0-7
  ldi  r16, 0				; r=0
  cpi  r22, 8
  brne p_red
  inc  r19
  rjmp p_blue

