; ****************************************************************************************
; *
; * Configure timing interrupt for VSync/HSync
; *
; ****************************************************************************************

init_sync:

  ldi  r16,(1<<CS10 | 1<<WGM12)			; TIMER1 (SCLK w/ RESET)
  sts  TCCR1B,r16

  ldi  r16,(1<<OCIE1A)		; TIMER1 MATCH A
  sts  TIMSK1,r16

  ldi  r16,hi8(636)			; 636 clock cycles (h-position)
  sts  OCR1AH,r16
  ldi  r16,lo8(636)
  sts  OCR1AL,r16

  ret


