#include <avr/io.h>
#include <avr/interrupt.h>

#define _DDRB   _SFR_IO_ADDR(DDRB)
#define _DDRD   _SFR_IO_ADDR(DDRD)
#define _PORTB  _SFR_IO_ADDR(PORTB)
#define _PORTD  _SFR_IO_ADDR(PORTD)
#define _SREG   _SFR_IO_ADDR(SREG)

.macro DELAY c				;(c*3)+2
  ldi  r16,\c				
  dec  r16				    
  brne .-4			    	
.endm

#define SRAM 0x0100
#define PAL  0x0200
#define BMP  0x0300

#define MODE SRAM + 0x10
#define SLOT SRAM + 0x12

#define V_START    34
#define V_END      514
#define BMP_YSIZE  0x1F		; 128x16
;#define BMP_YSIZE 0x0F		; 128x8

.global main

main:

  ; sync output pins
  sbi  _DDRB,1 				; vsync
  sbi  _DDRB,2 				; hsync

  ; RRRGGGBB output pins
  sbi  _DDRD,0 				; RED BIT 0
  sbi  _DDRD,1 				; RED BIT 1
  sbi  _DDRD,2 				; RED BIT 2
  sbi  _DDRD,3 				; GRN BIT 0
  sbi  _DDRD,4 				; GRN BIT 1
  sbi  _DDRD,5 				; GRN BIT 2
  sbi  _DDRD,6 				; BLU BIT 0
  sbi  _DDRD,7 				; BLU BIT 1

  ; disable NERDKITS UART
  ldi  r16,0
  sts  UCSR0B,r16

  ; build 256 color palette table
  call init_palette

  ; clear BMP table
  call init_bmp

  ; setup video interrupt
  call init_sync

  ; reset variables
  ldi  r26,lo8(SRAM)  		;XL=r26
  ldi  r27,hi8(SRAM)  		;XH=r27
  clr  r16
  st   x+,r16
  st   x+,r16
  st   x+,r16
  sts  SLOT,r16
  clr  r19
  clr  r22

  ; set mode
  ldi  r16,1
  sts  MODE,r16				;0=palette, 1=128x240 bitmap, 2=mixed

  ; config logo screen
  ldi  r16,16		 
  sts  BMP+15,r16
  sts  BMP+16,r16

  ; enable interrupts
  sei

 
