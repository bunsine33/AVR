  ; handle interrupt latency
  lds  r16,TCNT1L 			;2
  cpi  r16,10 				;1
  brlo LATFIX1 				;1/2
LATFIX1:
  cpi  r16,11 				;1
  brlo LATFIX2 				;1/2
LATFIX2:
  cpi  r16,12 				;1
  brlo LATFIX3 				;1/2
LATFIX3:

  ; hsync low
  cbi  _PORTB,2 			;2

  ; load registers from sram - 14 cycles (?)
  push r26 					;2
  push r27	 				;2
  ldi  r26,lo8(SRAM)	 	;1
  ldi  r27,hi8(SRAM) 		;1
  ld   r18,x+ 				;2 (1)?  vertical blank
  ld   r28,x+ 				;2 (1)?  vertical line lo8
  ld   r29,x+ 				;2 (1)?  vertical line hi8
  lds  r24,SLOT				;2 

  ; LINE COUNTER AND VERTICAL SYNC ON (12 cycles)
  ; TIME = 12 CYCLES
  adiw r28,1 				;2
  ldi  r16,lo8(525) 		;1
  ldi  r17,hi8(525) 		;1
  cp   r16,r28 				;1
  cpc  r17,r29 				;1
  breq s7 					;1/2
  nop 						;1
  nop 						;1
  nop 						;1
  nop						;1
  rjmp s8 					;2

s7:
  clr  r28 					;1
  clr  r29 					;1
  clr  r24					;1
  cbi  _PORTB,1				;2

s8:
  ; vsync off, line 2 (8 cycles)
  ldi  r16,lo8(2) 			;1
  ldi  r17,hi8(2) 			;1
  cp   r16,r28 				;1
  cpc  r17,r29 				;1
  breq s3 					;1/2
  nop 						;1
  rjmp s4 					;2

s3:
  sbi  _PORTB,1				;2

s4:
  ; pixels on, line 34 (v-position)
  ldi  r16,lo8(V_START) 	;1
  ldi  r17,hi8(V_START) 	;1
  cp   r16,r28 				;1
  cpc  r17,r29 				;1
  brne s5 					;1/2
  ldi  r18,1 				;1

s5:
  ; pixels off, line 514 (v-position)
  ldi  r16,lo8(V_END) 		;1
  ldi  r17,hi8(V_END) 		;1
  cp   r16,r28 				;1
  cpc  r17,r29 				;1
  brne s6 					;1/2
  ldi  r18,0 				;1

s6:
  ; calculate bitmap offset row for 128 x BMP_YSIZE
  movw r30,r28				;1
  sbiw r30,V_START			;2
  andi r30,BMP_YSIZE		;1
  lsr  r30					;1
  mov  r23,r30 				;1

  ; save registers to SRAM (12 cycles)
  st   -x,r29 				;2
  st   -x,r28 				;2
  st   -x,r18 				;2
  pop  r27 					;2
  pop  r26 					;2

  ; hsync high
  sbi  _PORTB,2 			;2

  ; exit on vertical blanking
  cpi  r18,0 				;1
  brne s9 					;1/2
  rjmp NOVID 				;2

s9:
  lds  r16,MODE				;1
  cpi  r16,0				;1
  breq palette				;1/2
  rjmp bitmap				;2

